#pragma GCC optimize("O3")
#pragma GCC optimize("unroll-loops")
#include <bits/stdc++.h>

using namespace std;
using ll = long long;


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, q;
    cin >> n >> q;
    for (int i = 0; i < n; ++i) {
        int l, r;
        cin >> l >> r;
    }

    while (q--) {
        int k;
        cin >> k;
        cout <<  k - 1 << ' ';
    }

    
    return 0;

}