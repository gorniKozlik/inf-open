#include <bits/stdc++.h>

using namespace std;
using ll = long long;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    ll n, q;
    cin >> n >> q;
    for (ll i = 0; i < n; ++i) {
        ll l;
        cin >> l >> l;
    }
    while (q--) {
        ll k;
        cin >> k;
        cout << k<<' ';
    }
}
